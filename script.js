function toggleBlockItem(index){
  if(index<0||index>=inventory.length)return;
  const item=inventory[index];
  if(blockedItems.has(item.id))blockedItems.delete(item.id);
  else blockedItems.add(item.id);
  saveData();
  showInventory();
}

