    alert(`–ù–µ–¥–æ—Å—Ç–∞—Ç–Ω—å–æ –Ω—ñ–∫—É—Å—ñ–≤! –ü–æ—Ç—Ä—ñ–±–Ω–æ ${totalPrice}, —É –≤–∞—Å ${balance}.`);
