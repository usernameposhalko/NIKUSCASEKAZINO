function weightedRandom(items) {
  let total = 0;
  items.forEach(i => total += i.chance);
  let r = Math.random() * total;
  for (let i of items) {
    if (r < i.chance) return i;
    r -= i.chance;
  }
  return items[0];
}

function getQuality() {
  const qualities = [
    { name: "–ü—Ä—è–º–æ –∑ —Ü–µ—Ö—É", chance: 0.125 },
    { name: "–ü—ñ—Å–ª—è –∫–æ–Ω—Å–µ—Ä–≤–∞—Ü—ñ—ó", chance: 0.25 },
    { name: "–ü—ñ—Å–ª—è —É—Ä–æ–∫—É", chance: 0.4 },
    { name: "–ó–Ω–æ—à–µ–Ω–∞", chance: 0.225 }
  ];
  let r = Math.random();
  let acc = 0;
  for (let q of qualities) {
    acc += q.chance;
    if (r < acc) return q.name;
  }
  return "–ó–Ω–æ—à–µ–Ω–∞";
}

